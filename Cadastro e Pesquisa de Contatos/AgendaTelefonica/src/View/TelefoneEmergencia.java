    public void exibeContatosEmergencia() {
        try {
            Collection<ContatoEmergencia> cont = gerenciador.getContatosEmergencia();
            String msg = "";

            for (ContatoEmergencia cont1 : cont) {
                msg += cont1.imprimeEmergencia()
                        + "\n--------------------------------------------";
            }

            jTAEmergencia.setText(msg);
        } catch (IllegalArgumentException iae) {
            jTAConsole.setText("Não existem registros");
            jTAEmergencia.setText("");
        }
    }

    public void limpaCampos() {
        jTFNomeEmergencia.setText("");
        jFTFTelefoneEmergencia.setText("");
    }

    private int criaAviso(String mensagem, String[] opcoes) {
        return JOptionPane.showOptionDialog(this, mensagem, "Contatos de Emergência", 0, JOptionPane.WARNING_MESSAGE, null, opcoes,
                opcoes[0]);
    }

