    private void jBCadastraEmergenciaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBCadastraEmergenciaActionPerformed
        try {
            nome = jTFNomeEmergencia.getText();
            telefone = jFTFTelefoneEmergencia.getText();

            ContatoEmergencia novo = gerenciador.incluirContatoEmergencia(nome, telefone);

            jTAConsole.setText(nome + " cadastrado com sucesso");
            this.limpaCampos();
            this.exibeContatosEmergencia();

        } catch (IllegalArgumentException iae) {
            jTAConsole.setText(iae.getMessage());
        } catch (IOException ex) {
            jTAConsole.setText("Não foi possivel cadastrar o contato");
        }
    }//GEN-LAST:event_jBCadastraEmergenciaActionPerformed

    private void jBExcluiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBExcluiActionPerformed
        int resposta = criaAviso("Deseja realmente excluir TODOS os registros?", opcoesAviso);

        if (resposta == 0) {
            try {
                gerenciador.excluirContatosEmergencia();
                jTAConsole.setText("Contatos excluidos com sucesso");
                this.exibeContatosEmergencia();

            } catch (IOException ex) {
                jTAConsole.setText("Falha ao excluir registros");
            }
        }
    }//GEN-LAST:event_jBExcluiActionPerformed

