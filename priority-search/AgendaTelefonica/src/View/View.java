    private void jBCadastraContatoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBCadastraContatoActionPerformed
        try {
            nome = jTFNomeContato.getText();
            telefone1 = jFTFTelefoneCadastro1.getText();
            telefone2 = jFTFTelefoneCadastro2.getText() + "";
            descricao = jTFobservacao.getText() + "";
            categoria = (String) jCBTipoCadastro.getSelectedItem();

            Contato novo = gerenciador.incluirContato(nome, telefone1, telefone2, descricao, categoria);

            jTAConsole.setText(novo.getNome() + " cadastrado com sucesso");
            this.limpaCampos();

        } catch (IOException ex) {
            jTAConsole.setText("Erro ao cadastrar contato.");
        } catch (IllegalArgumentException iae) {
            jTAConsole.setText(iae.getMessage());
        }
    }//GEN-LAST:event_jBCadastraContatoActionPerformed

