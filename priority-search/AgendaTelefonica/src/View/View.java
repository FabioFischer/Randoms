    private void jBExibeContatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBExibeContatosActionPerformed
        try {
            Collection<Contato> contatos = gerenciador.getContatos();
            String msg = "";

            for (Contato contato : contatos) {
                msg += contato.imprimeContato()
                        + "\n-------------------------------------------------------------------------------------------";
            }

            jTAMostraContatos.setText(msg);
        } catch (IllegalArgumentException iae) {
            jTAConsole.setText(iae.getMessage());
        }
    }//GEN-LAST:event_jBExibeContatosActionPerformed

    private void jBExcluirRegistrosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBExcluirRegistrosActionPerformed
        int resposta = criaAviso("Deseja realmente excluir TODOS os registros?", opcoesAviso);

        if (resposta == 0) {
            try {
                gerenciador.excluirContatos();
                jTAMostraContatos.setText("");
                jTAConsole.setText("Contatos excluidos com sucesso");

            } catch (IOException ex) {
                jTAConsole.setText("Falha ao excluir registros");
            }
        }
    }//GEN-LAST:event_jBExcluirRegistrosActionPerformed

